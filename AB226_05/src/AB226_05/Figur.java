package AB226_05;

public class Figur {
	protected int posx, posy;
	

	public Figur(int posx, int posy) {
		super();
		this.posx = posx;
		this.posy = posy;
	}
	
	
	
	/**
	 * @return the posx
	 */
	public int getPosx() {
		return posx;
	}

	/**
	 * @param posx the posx to set
	 */
	public void setPosx(int posx) {
		this.posx = posx;
	}

	/**
	 * @return the posy
	 */
	public int getPosy() {
		return posy;
	}

	/**
	 * @param posy the posy to set
	 */
	public void setPosy(int posy) {
		this.posy = posy;
	}







	
}
