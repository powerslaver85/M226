public class ArithmOp 
{
	public static void main(String[] args)
	{
		int z = 13;
		int n = 5;
		int a = 5;
		
		System.out.println("z / n: " + z / n);
		System.out.println("z % n: " + z % n);
		System.out.println("++a: " + ++a + " a: " + a);
		System.out.println("a++: " + a++ + " a: " + a);
		System.out.println("--a: " + --a + " a: " + a);
		System.out.println("a--: " + a-- + " a: " + a);
	}
	
}